def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build a PRD evidence pack for iM One NL2SQL.")
    parser.add_argument("--output-dir", default="logs/evidence_pack")
    parser.add_argument("--db-path", default="data/im_one_demo.sqlite")
    parser.add_argument("--audit-path", default="logs/evidence_audit.jsonl")
    parser.add_argument("--role", default="sales_planning")
    parser.add_argument("--branch-id", type=int, default=1)
    parser.add_argument("--profile", choices=("poc", "pilot"), help="Apply a readiness profile.")
    parser.add_argument("--live-checks", action="store_true", help="Enable live LLM/embedding checks requested by the profile.")
    parser.add_argument(
        "--strict",
        action="store_true",
        help="Exit non-zero when required readiness or PRD evaluation gates fail.",
    )
    parser.add_argument(
        "--case-group",
        action="append",
        choices=sorted({evaluation_case_group(case) for case in EVALUATION_CASES}),
        help="Include only evaluation cases in this group. Repeatable.",
    )
    parser.add_argument("--case-id", action="append", help="Include only a specific evaluation case id. Repeatable.")
    parser.add_argument("--blocked-only", action="store_true", help="Include only blocked safety cases.")
    parser.add_argument("--non-blocked-only", action="store_true", help="Include only non-blocked cases.")
    return parser.parse_args()


def select_cases(args: argparse.Namespace) -> tuple[EvaluationCase, ...]:
    cases = tuple(EVALUATION_CASES)
    if args.case_group:
        selected_groups = set(args.case_group)
        cases = tuple(case for case in cases if evaluation_case_group(case) in selected_groups)
    if args.case_id:
        selected_ids = set(args.case_id)
        known_ids = {case.case_id for case in EVALUATION_CASES}
        unknown_ids = sorted(selected_ids - known_ids)
        if unknown_ids:
            raise SystemExit("Unknown evaluation case id(s): " + ", ".join(unknown_ids))
        cases = tuple(case for case in cases if case.case_id in selected_ids)
    if args.blocked_only and args.non_blocked_only:
        raise SystemExit("--blocked-only and --non-blocked-only cannot be used together.")
    if args.blocked_only:
        cases = tuple(case for case in cases if case.should_block)
    if args.non_blocked_only:
        cases = tuple(case for case in cases if not case.should_block)
    if not cases:
        raise SystemExit("No evaluation cases matched the selected filters.")
    return cases


def readiness_report(db_path: str, profile: str | None, live_checks: bool) -> dict[str, Any]:
    requirements = preflight_requirements_for_profile(profile)
    profile_requests_live = bool(requirements.get("check_llm") or requirements.get("check_embedding"))
    if not live_checks:
        requirements["check_llm"] = False
        requirements["check_embedding"] = False
    checks = run_preflight(db_path=db_path, **requirements)
    report = build_preflight_report(checks, profile=profile, db_path=db_path)
    report["profile_applied"] = profile is not None
    report["live_checks_enabled"] = live_checks
    report["live_checks_requested"] = live_checks and profile_requests_live
    return report


def write_json(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def write_audit_log_artifact(source_path: str, destination_path: Path) -> dict[str, Any]:
    source = Path(source_path)
    if source.exists():
        content = source.read_text(encoding="utf-8")
    else:
        content = ""
    destination_path.parent.mkdir(parents=True, exist_ok=True)
    destination_path.write_text(content, encoding="utf-8")
    event_count = sum(1 for line in content.splitlines() if line.strip())
    return {
        "source_path": str(source),
        "artifact_path": destination_path.name,
        "source_exists": source.exists(),
        "event_count": event_count,
    }


