def validate_sql_for_evidence(
    *,
    db_path: str,
    sql: str,
    role: str,
    branch_id: int | None = None,
) -> Any:
    connection = connect_database(db_path)
    try:
        normalized_role = normalize_user_role(role)
        branch_scope_branch_id = branch_id if normalized_role == "branch_manager" else None
        return validate_sql(
            sql,
            allowed_tables=ROLE_TABLE_POLICY[normalized_role],
            connection=connection,
            branch_scope_branch_id=branch_scope_branch_id,
        )
    finally:
        connection.close()


