def run_live_llm_generation_sample_case(
    *,
    case: EvaluationCase,
    db_path: str,
    connection: sqlite3.Connection,
    role: str,
    branch_id: int,
) -> dict[str, Any]:
    started_at = perf_counter()
    context = retrieve_schema(case.question, user_role=role)
    base_case: dict[str, Any] = {
        "case_id": case.case_id,
        "question": case.question,
        "intent": case.intent,
        "expected_metric": case.expected_metric,
        "required_tables": list(case.required_tables),
        "expected_result_shape": list(case.expected_result_shape),
        "selected_tables": list(context.allowed_table_names),
        "matched_metrics": [metric.name for metric in context.matched_metrics],
        "retrieval_confidence": context.retrieval_confidence,
        **build_live_prompt_contract_case(case=case, role=role, branch_id=branch_id),
    }
    try:
        generated = generate_sql_with_llm(
            case.question,
            context,
            user_role=role,
            branch_id=branch_id,
        )
    except LLMGenerationError as exc:
        return {
            **base_case,
            "status": "failed",
            "elapsed_ms": round((perf_counter() - started_at) * 1000, 2),
            "error": str(exc),
        }

    validation = validate_sql(
        generated.sql,
        allowed_tables=context.allowed_table_names,
        connection=connection,
    )
    missing_tables = tuple(table for table in case.required_tables if table not in validation.referenced_tables)
    columns: tuple[str, ...] = ()
    row_count = 0
    execution_error = ""
    execution_payload: dict[str, Any] | None = None
    if validation.allowed:
        execution_result = execution_backend_for_name().execute_validated_sql(
            db_path=db_path,
            sql=validation.sql,
            timeout_ms=LIVE_LLM_QUERY_TIMEOUT_MS,
            allowed_tables=context.allowed_table_names,
        )
        execution_payload = summarize_query_execution_result(execution_result)
        columns = tuple(execution_result.columns)
        row_count = len(execution_result.rows)
        execution_error = execution_result.error_issue or ""
    missing_columns = tuple(column for column in case.expected_result_shape if column not in columns)
    elapsed_ms = round((perf_counter() - started_at) * 1000, 2)
    case_passed = (
        generated.engine == "llm"
        and generated.prompt_version == PROMPT_VERSION
        and validation.allowed
        and execution_payload is not None
        and not missing_tables
        and not missing_columns
        and not execution_error
        and row_count > 0
        and execution_payload["pre_execution_row_count_status"] == "checked"
        and elapsed_ms <= 10000
    )
    return {
        **base_case,
        "status": "passed" if case_passed else "failed",
        "elapsed_ms": elapsed_ms,
        "generation_engine": generated.engine,
        "model": generated.model,
        "prompt_version": generated.prompt_version,
        "reason": generated.reason,
        "assumptions": list(generated.assumptions),
        "sql": generated.sql,
        "validation_allowed": validation.allowed,
        "validation_issues": list(validation.issues),
        "referenced_tables": list(validation.referenced_tables),
        "missing_tables": list(missing_tables),
        "columns": list(columns),
        "missing_columns": list(missing_columns),
        "row_count": row_count,
        "execution": execution_payload,
        "execution_error": execution_error,
    }


def pattern_contract(content: str, patterns: tuple[str, ...]) -> dict[str, Any]:
    missing = [pattern for pattern in patterns if pattern not in content]
    return {
        "passed": not missing,
        "total": len(patterns),
        "matched": len(patterns) - len(missing),
        "missing": missing,
    }


def build_evidence_gate_payload(
    readiness: dict[str, Any],
    external_readiness: dict[str, Any],
    prd_thresholds: dict[str, Any],
) -> dict[str, Any]:
    failures: list[dict[str, Any]] = []
    readiness_required_failed = int(readiness["summary"]["required_failed"])
    external_missing_total = int(external_readiness["missing_total"])

    if readiness_required_failed:
        failures.append(
            {
                "name": "readiness_required_failures",
                "count": readiness_required_failed,
                "details": readiness["summary"]["required_failed_names"],
            }
        )
    if external_missing_total:
        failures.append(
            {
                "name": "external_readiness_missing",
                "count": external_missing_total,
                "details": external_readiness["missing_names"],
            }
        )
    if not prd_thresholds["passed"]:
        failures.append(
            {
                "name": "prd_evaluation_gate_failed",
                "count": len(prd_thresholds["failures"]),
                "details": prd_thresholds["failures"],
            }
        )

    return {
        "passed": not failures,
        "status": "passed" if not failures else "failed",
        "failure_count": len(failures),
        "failures": failures,
    }


def completion_status_for_requirement(requirement: dict[str, Any]) -> str:
    statuses = [check["status"] for check in requirement.get("preflight_checks", [])]
    if any(status == "failed" for status in statuses):
        return "failed"
    if any(status == "not_run" for status in statuses):
        return "not_run"
    if statuses and all(status == "passed" for status in statuses):
        return "passed"
    return "unknown"


def build_completion_blocking_conditions(
    evidence_gate: dict[str, Any],
    external_readiness: dict[str, Any],
    prd_thresholds: dict[str, Any],
) -> list[dict[str, Any]]:
    blocking_conditions: list[dict[str, Any]] = []
    for failure in evidence_gate.get("failures", []):
        condition: dict[str, Any] = {
            "name": failure["name"],
            "count": failure["count"],
            "details": failure["details"],
        }
        if failure["name"] == "external_readiness_missing":
            condition["verification_commands"] = external_readiness.get("missing_verification_commands", [])
        if failure["name"] == "prd_evaluation_gate_failed":
            condition["thresholds"] = prd_thresholds.get("targets", {})
        blocking_conditions.append(condition)
    return blocking_conditions


