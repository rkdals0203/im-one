def build_live_llm_generation_samples_artifact(
    *,
    db_path: str,
    role: str,
    branch_id: int,
    live_checks: bool,
) -> dict[str, Any]:
    core_cases = tuple(case for case in EVALUATION_CASES if evaluation_case_group(case) == "core")
    base_payload: dict[str, Any] = {
        "status": "not_run",
        "live_checks_enabled": live_checks,
        "required_core_cases": 5,
        "case_total": len(core_cases),
        "passed_cases": 0,
        "failed_cases": 0,
        "model": configured_llm_model(),
        "base_url": configured_llm_base_url(),
        "cases": [],
    }
    if not live_checks:
        return {
            **base_payload,
            "reason": "Live LLM checks were not requested for this evidence run.",
        }
    if len(core_cases) < 5:
        return {
            **base_payload,
            "status": "failed",
            "reason": f"Core demo case count is {len(core_cases)}, below required 5.",
        }
    if not llm_endpoint_configured():
        return {
            **base_payload,
            "status": "failed",
            "failed_cases": len(core_cases),
            "reason": "LLM endpoint is not configured.",
            "cases": [
                {
                    **build_live_prompt_contract_case(case=case, role=role, branch_id=branch_id),
                    "case_id": case.case_id,
                    "question": case.question,
                    "status": "failed",
                    "error": "LLM endpoint is not configured.",
                }
                for case in core_cases
            ],
        }

    ensure_demo_database(db_path)
    case_results: list[dict[str, Any]] = []
    connection = connect_database(db_path)
    try:
        for case in core_cases:
            case_results.append(
                run_live_llm_generation_sample_case(
                    case=case,
                    db_path=db_path,
                    connection=connection,
                    role=role,
                    branch_id=branch_id,
                )
            )
    finally:
        connection.close()

    passed_cases = sum(1 for item in case_results if item["status"] == "passed")
    return {
        **base_payload,
        "status": "passed" if passed_cases == len(core_cases) else "failed",
        "passed_cases": passed_cases,
        "failed_cases": len(core_cases) - passed_cases,
        "reason": "Core demo live LLM samples executed.",
        "cases": case_results,
    }


def build_live_prompt_contract_case(
    *,
    case: EvaluationCase,
    role: str,
    branch_id: int,
) -> dict[str, Any]:
    context = retrieve_schema(case.question, user_role=role)
    payload = build_llm_payload(
        case.question,
        context,
        model=configured_llm_model(),
        user_role=role,
        branch_id=branch_id,
    )
    payload_bytes = json.dumps(payload, ensure_ascii=False, sort_keys=True).encode("utf-8")
    user_context = json.loads(str(payload["messages"][1]["content"]))
    response_contract = user_context.get("response_contract", {})
    if not isinstance(response_contract, dict):
        response_contract = {}
    properties = response_contract.get("properties", {})
    if not isinstance(properties, dict):
        properties = {}
    assumptions_property = properties.get("assumptions", {})
    if not isinstance(assumptions_property, dict):
        assumptions_property = {}
    assumptions_items = assumptions_property.get("items", {})
    if not isinstance(assumptions_items, dict):
        assumptions_items = {}
    return {
        "prompt_payload_sha256": hashlib.sha256(payload_bytes).hexdigest(),
        "prompt_contract": {
            "model": payload["model"],
            "prompt_version": user_context.get("prompt_version"),
            "temperature": payload.get("temperature"),
            "top_p": payload.get("top_p"),
            "response_format": payload["response_format"],
            "response_contract_required": response_contract.get("required", []),
            "response_contract_checks": {
                "required_keys": response_contract.get("required") == [
                    "sql",
                    "reason",
                    "assumptions",
                ],
                "sql_is_string": properties.get("sql", {}).get("type") == "string"
                if isinstance(properties.get("sql", {}), dict)
                else False,
                "reason_is_string": properties.get("reason", {}).get("type") == "string"
                if isinstance(properties.get("reason", {}), dict)
                else False,
                "assumptions_is_string_array": assumptions_property.get("type") == "array"
                and assumptions_items.get("type") == "string",
            },
            "selected_table_names": [
                table["name"]
                for table in user_context.get("selected_schema", {}).get("allowed_tables", [])
                if isinstance(table, dict) and "name" in table
            ],
            "matched_metric_names": [
                metric["name"]
                for metric in user_context.get("matched_metrics", [])
                if isinstance(metric, dict) and "name" in metric
            ],
        },
    }


